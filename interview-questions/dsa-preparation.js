// Data Structure Types (DS);

// Linear: Data elements are arranged sequentially.
// Arrays: Fixed-size, ordered collections.
// Linked Lists: Dynamic, non-contiguous nodes (Singly, Doubly, Circular).
// Stacks: LIFO (Last In, First Out) structure.
// Queues: FIFO (First In, First Out) structure (Simple, Circular, Priority).
// Non-Linear: Data elements are arranged hierarchically or in networks.
// Trees: Hierarchical structure (Binary Tree, Binary Search Tree, AVL, B-Tree).
// Graphs: Network of nodes connected by edges (Directed, Undirected).
// Hash-Based: Hash Tables for fast, key-value lookup. Algorithm Types (Algorithms)
// Sorting: Arranging data (Bubble, Merge, Quick, Insertion).
// Searching: Finding data (Linear, Binary).
// Techniques:
// Divide and Conquer: Breaking problems into smaller, solvable pieces (Merge Sort).
// Greedy Algorithms: Making optimal local choices (Dijkstra's, Prim's).
// Dynamic Programming: Solving complex problems by breaking them into overlapping subproblems.
// Recursion & Backtracking: Algorithms that call themselves to explore solutions. 

